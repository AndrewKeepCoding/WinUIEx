namespace WinUIEx
{
    /// <summary>
    /// The backdrop type to apply to a <see cref="WindowEx"/> window.
    /// </summary>
    /// <seealso cref="WindowEx.Backdrop"/>
    public enum Backdrop
    {
        /// <summary>
        /// No backdrop applied
        /// </summary>
        Default,

        /// <summary>
        /// Acrylic semi-transparent backdrop
        /// </summary>
        Acrylic,

        /// <summary>
        /// Mica backdrop
        /// </summary>
        Mica
    }
}
